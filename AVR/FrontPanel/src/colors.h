/**
 * @file colors.h
 * 16 bit color definitions for use with the OLED
 */

#ifndef __COLORS_H__
#define __COLORS_H__

// Color definitions
#define	BLACK       0x0000
#define	BLUE        0x001F
#define	RED         0xF800
#define	GREEN       0x07E0
#define CYAN        0x07FF
#define MAGENTA     0xF81F
#define YELLOW      0xFFE0
#define WHITE      0xFFFF

#endif
